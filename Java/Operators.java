public class Operators {
    public static void main(String args[]){
        int a= 10;
        int d= 10;
        int c= 0;
        int b= 0;
        //Post increment;
        b= a++;
        System.out.println("The value of b in pre increment: "+b);
        System.out.println("The value of a in pre increment: "+a);

        //Pre increment;
        c= ++d;
        System.out.println("The value of d in pre increment: "+d);
        System.out.println("The value of c in pre Increment: "+c);
        
    }
    
}